burger = document.querySelector('.burger');
navbar = document.querySelector('.navbar');
navlist = document.querySelector('.navlist');

burger.addEventListener('click', () => {
    navlist.classList.toggle('vclass');
    navbar.classList.toggle('hnav');
})

function sem1()
    {
    let showhidesem1=document.getElementById('showhidesem1');
    let btnshowhide=document.getElementById('btnshowhide');
    if(showhidesem1.style.display!='none')
    {
        showhidesem1.style.display='none';
    }
    else{
        showhidesem1.style.display='block';
    }
    }

